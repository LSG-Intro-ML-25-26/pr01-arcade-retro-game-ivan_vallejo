def confirm_choice(pokemon_npc: NPC):
    global has_pokemon
    p_name = pokemon_npc.name
    
    chosen_img = None
    if p_name == "Bulbasaur":
        chosen_img = IMG_BULBASAUR
    elif p_name == "Charmander":
        chosen_img = IMG_CHARMANDER
    elif p_name == "Squirtle":
        chosen_img = IMG_SQUIRTLE

    if not chosen_img:
        chosen_img = assets.image("""pokeball""")

    preview = sprites.create(chosen_img, SpriteKind.food)
    preview.z = 200
    preview.set_position(80, 26)
    preview.set_flag(SpriteFlag.RELATIVE_TO_CAMERA, True)
    
    pause(100)

    if game.ask("¿Elegir a " + p_name + "?"):
        preview.destroy()
        has_pokemon = True
        player_choice = p_name
        music.play(music.melody_playable(music.ba_ding), music.PlaybackMode.IN_BACKGROUND)
        game.show_long_text("¡Has conseguido a " + p_name + "!", DialogLayout.BOTTOM)
        
        pokemon_npc.sprite.destroy()
        npc_list.remove_element(pokemon_npc)
        control.run_in_parallel(rival_scene)
    else:
        preview.destroy()

